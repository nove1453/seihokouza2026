def tidy(value: str) -> str:
    value = re.sub(r"===== PDF PAGE \d+ =====", "", value)
    value = re.sub(r"[ \t]+\n", "\n", value)
    value = re.sub(r"\n[ \t]+", "\n", value)
    value = re.sub(r"(?<=[^\n。！？])\n(?=[^\n])", "", value)
    value = re.sub(r"\n{3,}", "\n\n", value)
    return value.strip()


def page_text(page: int) -> str:
    return EXAM.split(f"===== PDF PAGE {page} =====", 1)[1].split(f"===== PDF PAGE {page + 1} =====", 1)[0]


def options_from(block: str) -> list[dict]:
    matches = list(re.finditer(r"(?m)^([アイウエオカキクケコ])．", block))
    result = []
    for index, match in enumerate(matches):
        end = matches[index + 1].start() if index + 1 < len(matches) else len(block)
        option = tidy(block[match.end():end])
        option = re.split(r"\n\s*(?:＜|語群|copyright)", option)[0].strip()
        result.append({"label": match.group(1), "text": option})
    return result


def topic_meta(title: str):
    if "危険評価額" in title or "危険と保険" in title:
        return "生命保険の仕組み", "危険と保険", "2-6"
    if "変額保険" in title:
        return "生命保険と社会経済", "資産運用の原則", "108-110"
    for keyword, category, section, page in TOPIC_RULES:
        if keyword in title:
            return category, section, page
    return "生命保険の仕組み", title.strip() or "要確認", "要確認"


def parse_form(form: str) -> list[dict]:
    full_form = {"B": "Ｂ", "C": "Ｃ"}[form]
    chunk = EXAM.split(f"試 験 問 題【 フ ォ ー ム{full_form} 】", 1)[1]
    chunk = chunk.split(f"【フォーム{full_form}】 解答一覧", 1)[0]
    clean_chunk = re.sub(r"===== PDF PAGE \d+ =====", "", chunk)
    answers = {number: ANSWERS[form][number - 1] for number in range(1, 51)}
    questions = []

    for group_index, page in enumerate(GROUP_PAGES[form]):
        block = page_text(page)
        numbers = range(group_index * 5 + 1, group_index * 5 + 6)
        stem = block.split("語群", 1)[0]
        stem = re.sub(r"^\s*［[^\n]+］\s*", "", stem, count=1)
        stem = re.sub(r"文中の空欄.*?選んでください。\s*", "", stem, count=1, flags=re.S)
        stem = tidy(stem)
        choices = options_from(block.split("語群", 1)[1])
        for number in numbers:
            category, section, page_ref = GROUP_META[form][number]
            answer = answers[number]
            answer_text = next(choice["text"] for choice in choices if choice["label"] == answer)
            summary = GROUP_SUMMARIES[form][number]
            questions.append(make_question(
                form, number, "語群選択", category, section, page_ref,
                f"次の文章の空欄［{number}］に入る最も適切なものを選んでください。\n\n{stem}",
                choices, answer, answer_text, summary,
                f"空欄［{number}］は「{answer_text}」。前後の語句とのつながりまで覚える。",
            ))

    markers = []
    for number in range(21, 51):
        match = re.search(rf"(?:［\s*{number}\s*］|(?m:^{number}［))", clean_chunk)
        if not match:
            raise RuntimeError(f"Form {form}: marker {number} not found")
        markers.append((number, match.start()))

    for index, (number, start) in enumerate(markers):
        end = markers[index + 1][1] if index + 1 < len(markers) else len(clean_chunk)
        block = clean_chunk[start:end].strip()
        block = re.sub(rf"^(?:［\s*{number}\s*］|{number}［)", "", block)
        if number <= 30:
            title, rest = block.split("\n", 1)
            rest = re.sub(r"^次の文章のうち、.*?選んでください。\s*", "", rest, count=1, flags=re.S)
            choices = options_from(rest)
            question_text = title.strip()
        elif number <= 40:
            title = block.split("］", 1)[0].strip()
            rest = block.split("］", 1)[1]
            rest = re.sub(r"^\s*次の文章について、.*?選んでください。\s*", "", rest, count=1, flags=re.S)
            rest = re.sub(r"\n正\s*\n誤\s*.*$", "", rest, flags=re.S)
            choices = [{"label": "正", "text": "正しい"}, {"label": "誤", "text": "誤っている"}]
            question_text = f"{title}\n\n{tidy(rest)}"
        else:
            title = block.split("］", 1)[0].strip()
            rest = block.split("］", 1)[1]
            rest = re.sub(r"^\s*次の文章について、.*?記号Ｃ\s*を選択してください。\s*", "", rest, count=1, flags=re.S)
            choices = options_from(rest)
            first_option = re.search(r"(?m)^ア．", rest)
            question_text = f"{title}\n\n{tidy(rest[:first_option.start()])}"

        answer = answers[number]
        answer_text = next(choice["text"] for choice in choices if choice["label"] == answer)
        category, section, page_ref = topic_meta(title)
        question_type = (
            "正しいものを1つ選ぶ" if number <= 25 else
            "誤っているものを1つ選ぶ" if number <= 30 else
            "正誤問題" if number <= 40 else
            "適切な組み合わせ"
        )
        summary = STATEMENT_SUMMARIES[form][number]
        questions.append(make_question(
            form, number, question_type, category, section, page_ref,
            question_text, choices, answer, answer_text, summary, summary,
        ))

    questions.sort(key=lambda question: question["questionNumber"])
    return questions


def make_question(form, number, question_type, category, section, page, question, choices, answer, answer_text, explanation, key_point):
    return {
        "id": f"2022-{form}-{number:03d}",
        "year": 2022,
        "form": form,
        "subject": "生命保険総論",
        "questionNumber": number,
        "questionType": question_type,
        "category": category,
        "chapter": CHAPTERS[category],
        "section": section,
        "question": question,
        "choices": choices,
        "answer": answer,
        "answerText": answer_text,
        "explanation": explanation,
        "keyPoint": key_point,
        "commonMistake": "主体・数値・増減・制度名など、正誤を分ける語句をテキスト根拠と照合する。",
        "difficulty": "標準",
        "tags": ["2022年度", f"フォーム{form}"],
        "evidence": {
            "textbook": "2026年版 生命保険講座 生命保険総論",
            "page": page,
            "chapter": CHAPTERS[category],
            "section": section,
            "quote": explanation,
        },
    }


for form in ("B", "C"):
    output = parse_form(form)
    expected_counts = {n: 10 if n <= 20 else 3 if n <= 30 else 2 if n <= 40 else 5 for n in range(1, 51)}
    assert len(output) == 50
    assert all(len(question["choices"]) == expected_counts[question["questionNumber"]] for question in output)
    assert all(any(choice["label"] == question["answer"] for choice in question["choices"]) for question in output)
    destination = ROOT / f"data/questions-2022-{form.lower()}.json"
    destination.write_text(json.dumps(output, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"wrote {len(output)} questions to {destination}")
